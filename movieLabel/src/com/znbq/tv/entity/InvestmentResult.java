package com.znbq.tv.entity;



public class InvestmentResult {

	
	private String averageRate;
	private String startTime;
	private String primeTeam;
	private String sexRange;
	private String ageRange;
	private String satisfied;

	
	public String getAverageRate() {
		return averageRate;
	}
	
	public void setAverageRate(String averageRate) {
		this.averageRate = averageRate;
	}
	public String getStartTime() {
		return startTime;
	}
	public void setStartTime(String startTime) {
		this.startTime = startTime;
	}
	public String getPrimeTeam() {
		return primeTeam;
	}
	public void setPrimeTeam(String primeTeam) {
		this.primeTeam = primeTeam;
	}
	public String getSexRange() {
		return sexRange;
	}
	public void setSexRange(String sexRange) {
		this.sexRange = sexRange;
	}
	public String getAgeRange() {
		return ageRange;
	}
	public void setAgeRange(String ageRange) {
		this.ageRange = ageRange;
	}
	public String getSatisfied() {
		return satisfied;
	}
	public void setSatisfied(String satisfied) {
		this.satisfied = satisfied;
	}
	
	
}
